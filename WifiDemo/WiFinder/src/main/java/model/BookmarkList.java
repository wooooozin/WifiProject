package model;

public class BookmarkList {
	private String bookmarkListId;
	private String bookmarkName;
	private String wifiMainName;
	private String regitDateS;
	
	public String getBookmarkListId() {
		return bookmarkListId;
	}
	
	public void setBookmarkListId(String bookmarkListId) {
		this.bookmarkListId = bookmarkListId;
	}
	
	public String getBookmarkName() {
		return bookmarkName;
	}
	
	public void setBookmarkName(String bookmarkName) {
		this.bookmarkName = bookmarkName;
	}
	
	public String getWifiMainName() {
		return wifiMainName;
	}
	
	public void setWifiMainName(String wifiMainName) {
		this.wifiMainName = wifiMainName;
	}
	
	public String getRegitDateS() {
		return regitDateS;
	}
	
	public void setRegitDateS(String regitDateS) {
		this.regitDateS = regitDateS;
	}
	
	
}
